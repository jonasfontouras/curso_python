num1 = int(input('Número 1: '))
num2 = int(input('Número 2: '))

soma = num1 + num2
sub = num1 - num2
mult = num1 * num2
div = num1 / num2

print('{} + {} = {}'.format(num1, num2, soma))
print('{} - {} = {}'.format(num1, num2, sub))
print('{} * {} = {}'.format(num1, num2, mult))
print('{} / {} = {}'.format(num1, num2, div))